function popcount(buf) {
  let n = 0;
  for (let i = 0; i < buf.length; i++) {
    let v = buf[i];
    while (v) { v &= v - 1; n++; }
  }
  return n;
}

// Generate one 20-byte case by XORing the base with counter bits at the
// positions named by `inc`, and (optionally) flipping a single position
// named by the shiftPos-th `1` bit of `shift`. shiftPos < 0 disables flip.
function genCase(base, incMask, shiftMask, counterVal, shiftPos) {
  const out = new Uint8Array(20);
  let nextIncBit = 0;
  let nextShiftIdx = 0;
  for (let b = 0; b < 20; b++) {
    let v = base[b];
    const im = incMask[b];
    const sm = shiftMask[b];
    for (let p = 0; p < 8; p++) {
      const bit = 1 << p;
      if (im & bit) {
        v ^= ((counterVal >> nextIncBit) & 1) << p;
        nextIncBit++;
      }
      if (sm & bit) {
        if (nextShiftIdx === shiftPos) v ^= bit;
        nextShiftIdx++;
      }
    }
    out[b] = v & 0xff;
  }
  return out;
}

function runOneCase(t, v, mem, cpu, crc) {
  // Same skip the .MAC harness does (lines 437-447): HLT alone, or any
  // Z80 IX/IY prefix (DD/FD) followed by HLT.
  if (v[0] === 0x76) return false;
  if ((v[0] & 0xdf) === 0xdd && v[1] === 0x76) return false;

  mem.write(IUT_ADDR,     v[0]);
  mem.write(IUT_ADDR + 1, v[1]);
  mem.write(IUT_ADDR + 2, v[2]);
  mem.write(IUT_ADDR + 3, v[3]);
  for (let i = 0; i < 16; i++) mem.write(MSBT + i, v[4 + i]);

  cpu.set_l(v[10]); cpu.set_h(v[11]);
  cpu.set_e(v[12]); cpu.set_d(v[13]);
  cpu.set_c(v[14]); cpu.set_b(v[15]);
  cpu.retrieve_flags(v[16]);
  cpu.set_a(v[17]);
  cpu.sp = v[18] | (v[19] << 8);

  cpu.pc = IUT_ADDR;
  cpu.instruction();

  const fMasked = cpu.store_flags() & t.flagMask;
  const hlLo = cpu.l(), hlHi = cpu.h();
  updcrc(crc, mem.read(MSBT));
  updcrc(crc, mem.read(MSBT + 1));
  updcrc(crc, hlLo); updcrc(crc, hlHi);   // iy alias
  updcrc(crc, hlLo); updcrc(crc, hlHi);   // ix alias
  updcrc(crc, hlLo); updcrc(crc, hlHi);   // real hl
  updcrc(crc, cpu.e()); updcrc(crc, cpu.d());
  updcrc(crc, cpu.c()); updcrc(crc, cpu.b());
  updcrc(crc, fMasked); updcrc(crc, cpu.a());
  updcrc(crc, cpu.sp & 0xff);
  updcrc(crc, (cpu.sp >> 8) & 0xff);
  return true;
}

function runDescriptor(t) {
  const mem = new Memory();
  const cpu = new I8080(mem, new IO());

  const numCounter = 1 << popcount(t.inc);
  const popShift = popcount(t.shift);
  // The .MAC's shift() routine has a stop bit one position past the last
  // `1` in shift_mask, so the shifter walks through popcount(shift)+1
  // positions (pos 0..popcount). The trailing position is "out of range" —
  // genCase produces no flip for it, equivalent to a second BASE-like case
  // appended to each counter cycle. For popcount(shift)=0 this collapses
  // to a single shift cycle (just the counter sweep).
  const numShift = popShift + 1;
  const total = numCounter * numShift;

  const crc = initcrc();
  let executed = 0;

  // Case 1: initial copy = BASE verbatim (no subyte applied).
  if (runOneCase(t, t.base, mem, cpu, crc)) executed++;

  // Cases 2..N: setup is called with the post-advance (counter, shifter_pos).
  // shifter_pos=0 only ever sees counter=1..numCounter-1 — counter=0 at
  // shifter_pos=0 is the BASE case above.
  for (let p = 0; p < numShift; p++) {
    const startC = (p === 0) ? 1 : 0;
    for (let c = startC; c < numCounter; c++) {
      const v = genCase(t.base, t.inc, t.shift, c, p);
      if (runOneCase(t, v, mem, cpu, crc)) executed++;
    }
  }

  return { crc, executed, total };
}

function runZex() {
  const hex = (b) => b.toString(16).padStart(2, '0');
  let allOk = true;
  let totalExecuted = 0;
  const overallStart = Date.now();
  for (const t of TESTS) {
    const padded = (t.name + ' ').padEnd(32, '.');
    const start = Date.now();
    const { crc, executed, total } = runDescriptor(t);
    const elapsed = ((Date.now() - start) / 1000).toFixed(2);
    const got = `${hex(crc[0])}${hex(crc[1])}${hex(crc[2])}${hex(crc[3])}`;
    const exp = `${hex(t.expected[0])}${hex(t.expected[1])}${hex(t.expected[2])}${hex(t.expected[3])}`;
    const ok = got === exp;
    totalExecuted += executed;
    // Single console.log per test — d8/jsc/spidermonkey have no
    // process.stdout, and console.log is the only print primitive that
    // works portably across every engine we target.
    if (ok) {
      console.log(`${padded} OK   ${got}  ${executed}/${total} cases  ${elapsed}s`);
    } else {
      console.log(`${padded} FAIL got ${got} expected ${exp}  ${executed}/${total} cases  ${elapsed}s`);
      allOk = false;
    }
  }
  const totalElapsed = ((Date.now() - overallStart) / 1000).toFixed(2);
  console.log(`---`);
  console.log(`${allOk ? 'PASS' : 'FAIL'}  ${totalExecuted} cases in ${totalElapsed}s`);
  return allOk;
}
