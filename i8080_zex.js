// CRC fold mirroring updcrc in 8080EX1.MAC lines 972-1009.
// crc is a Uint8Array(4); idx = byte ^ crc[3]; result is the standard
// byte-at-a-time right-shift CRC32 step.
function updcrc(crc, byte) {
  const idx = (byte ^ crc[3]) & 0xff;
  const off = idx << 2;
  const t0 = CRCTAB[off];
  const t1 = CRCTAB[off + 1];
  const t2 = CRCTAB[off + 2];
  const t3 = CRCTAB[off + 3];
  crc[3] = t3 ^ crc[2];
  crc[2] = t2 ^ crc[1];
  crc[1] = t1 ^ crc[0];
  crc[0] = t0;
}

function initcrc() {
  const c = new Uint8Array(4);
  c[0] = 0xff; c[1] = 0xff; c[2] = 0xff; c[3] = 0xff;
  return c;
}

// Encode a 20-byte test vector, mirroring the .MAC `tstr` macro:
//   db insn (1..4 bytes, padded with 0)
//   dw memop, hliy, hlix, hl, de, bc
//   db flags, acc
//   dw sp
function tstr(insn, memop, hliy, hlix, hl, de, bc, flags, acc, sp) {
  const r = new Uint8Array(20);
  if (Array.isArray(insn)) {
    for (let i = 0; i < insn.length && i < 4; i++) r[i] = insn[i] & 0xff;
  } else {
    r[0] = insn & 0xff;
  }
  // bytes 1..3 left as 0 if insn was a single byte
  let p = 4;
  const dw = (v) => { r[p++] = v & 0xff; r[p++] = (v >> 8) & 0xff; };
  dw(memop); dw(hliy); dw(hlix); dw(hl); dw(de); dw(bc);
  r[p++] = flags & 0xff;
  r[p++] = acc & 0xff;
  dw(sp);
  return r;
}

